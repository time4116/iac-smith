def _backend_bootstrap(env: str, change_plan: ChangePlan, intent: InfrastructureIntent) -> str:
    backend = change_plan.backend_resources[env]
    return f'''terraform {{
  required_version = ">= 1.6.0"

  required_providers {{
    aws = {{
      source  = "hashicorp/aws"
      version = "~> 5.0"
    }}
  }}
}}

provider "aws" {{
  region = "{intent.region}"
}}

resource "aws_s3_bucket" "tfstate" {{
  bucket = "{backend.bucket}"
}}

resource "aws_s3_bucket_versioning" "tfstate" {{
  bucket = aws_s3_bucket.tfstate.id
  versioning_configuration {{
    status = "Enabled"
  }}
}}

resource "aws_s3_bucket_server_side_encryption_configuration" "tfstate" {{
  bucket = aws_s3_bucket.tfstate.id

  rule {{
    apply_server_side_encryption_by_default {{
      sse_algorithm = "AES256"
    }}
  }}
}}

resource "aws_s3_bucket_public_access_block" "tfstate" {{
  bucket = aws_s3_bucket.tfstate.id

  block_public_acls       = true
  block_public_policy     = true
  ignore_public_acls      = true
  restrict_public_buckets = true
}}

resource "aws_dynamodb_table" "tflock" {{
  name         = "{backend.lock_table}"
  billing_mode = "PAY_PER_REQUEST"
  hash_key     = "LockID"

  attribute {{
    name = "LockID"
    type = "S"
  }}
}}
'''


def _workflow_check() -> str:
    return """name: Terraform PR Check

on:
  pull_request:
    paths:
      - 'environments/**'
      - 'modules/**'

permissions:
  id-token: write
  contents: read
  pull-requests: read

jobs:
  plan:
    name: Terragrunt Plan (non-prod)
    runs-on: ubuntu-latest
    steps:
      - name: Checkout
        uses: actions/checkout@v4

      - name: Check for non-prod changes
        id: filter
        uses: dorny/paths-filter@v3
        with:
          filters: |
            changed:
              - 'environments/non-prod/**'
              - 'modules/**'

      - name: Setup Terraform
        if: steps.filter.outputs.changed == 'true'
        uses: hashicorp/setup-terraform@v3
        with:
          terraform_version: "1.9.0"

      - name: Setup Terragrunt
        if: steps.filter.outputs.changed == 'true'
        uses: autero1/action-terragrunt@v3
        with:
          terragrunt-version: "0.58.0"

      - name: Configure AWS credentials
        if: steps.filter.outputs.changed == 'true'
        uses: aws-actions/configure-aws-credentials@v4
        with:
          role-to-assume: ${{ secrets.AWS_ROLE_ARN_NON_PROD }}
          aws-region: us-west-2
          audience: sts.amazonaws.com

      - name: Terragrunt Plan
        if: steps.filter.outputs.changed == 'true'
        run: terragrunt run-all plan --terragrunt-non-interactive -lock-timeout=20m
        working-directory: environments/non-prod
"""


