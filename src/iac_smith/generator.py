# Known module renderers keyed by stack name.
# have a specialised renderer fall back to a generic stub that provides the
# standard module scaffold for the PR reviewer to flesh out.
_MODULE_RENDERERS: dict[str, Callable[[], str]] = {
    "foundation": _vpc_main,
    "vpc": _vpc_main,
    "eks-fargate": _eks_fargate_main,
    "ecs-fargate": _ecs_fargate_main,
    "rds-postgres": _rds_postgres_main,
}


