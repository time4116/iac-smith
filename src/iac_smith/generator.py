def _header() -> str:
    return "# Generated by IaC Smith. Review before apply.\n"


def _repo_slug(target_repo: str) -> str:
    """Sanitised repo name for use in file titles and resource names."""
    import re

    name = target_repo.split("/")[-1]
    return re.sub(r"[^a-z0-9-]", "-", name.lower()).strip("-")[:40]


def _backend_resource(env: str) -> BackendResource:
    """Canonical backend names aligned with the bootstrap IAM policy.

    These names intentionally use the iac-smith-state-* and iac-smith-lock-*
    prefixes because the GitHub OIDC role is scoped to those resources.
    """
    return BackendResource(
        bucket=f"iac-smith-state-{env}-322264632107",
        lock_table=f"iac-smith-lock-{env}",
    )


def _root_terragrunt(intent: InfrastructureIntent) -> str:
    """Root live/terragrunt.hcl — region locals only.

    Remote state belongs in each env-level terragrunt.hcl so that non-prod and
    prod point at completely separate S3 buckets and DynamoDB lock tables.
    """
    return f'''{_header()}
locals {{
  aws_region = "{intent.region}"
}}
'''


def _env_terragrunt(env: str, intent: InfrastructureIntent, backend: BackendResource) -> str:
    """Per-environment terragrunt.hcl — owns the remote_state block for this env."""
    return f'''{_header()}
locals {{
  environment = "{env}"
  aws_region  = "{intent.region}"
}}

remote_state {{
  backend = "s3"
  config = {{
    bucket         = "{backend.bucket}"
    key            = "${{path_relative_to_include()}}/terraform.tfstate"
    region         = local.aws_region
    encrypt        = true
    dynamodb_table = "{backend.lock_table}"
  }}

  generate = {{
    path      = "backend.tf"
    if_exists = "overwrite_terragrunt"
  }}
}}
'''


def _foundation_dependency(stack_name: str) -> str:
    if not _uses_foundation(stack_name):
        return ""
    return """dependency "foundation" {
  config_path = "../foundation"

  mock_outputs_allowed_terraform_commands = ["validate", "plan"]
  mock_outputs = {
    vpc_id          = "vpc-00000000000000000"
    private_subnets = ["subnet-00000000000000000", "subnet-11111111111111111"]
    public_subnets  = ["subnet-22222222222222222", "subnet-33333333333333333"]
  }
}

"""


def _foundation_inputs(stack_name: str) -> str:
    if not _uses_foundation(stack_name):
        return ""
    return """
  vpc_id             = dependency.foundation.outputs.vpc_id
  private_subnet_ids = dependency.foundation.outputs.private_subnets
"""


def _stack_terragrunt(
    stack_name: str,
    env: str,
    intent: InfrastructureIntent,
    repo_patterns: RepoPatterns,
) -> str:
    """Generate a stack-level terragrunt.hcl.

    Calculates the relative source path based on the actual nesting depth
    (live/{env}/{stack_name} = 3 levels) so the ../../../modules/ reference is
    always correct regardless of how many path segments are in stack_name.
    """
    name_prefix = f"{stack_name}-{env}"
    # live/{env}/{stack_name}/terragrunt.hcl is 3 directories deep from live/
    depth = 3
    rel_source = "../" * depth + f"modules/{stack_name}"
    return f'''{_header()}
include "root" {{
  path = find_in_parent_folders()
}}

include "env" {{
  path   = find_in_parent_folders("{env}/terragrunt.hcl")
  expose = true
}}

terraform {{
  source = "{rel_source}"
}}

{_foundation_dependency(stack_name)}inputs = {{
  name_prefix = "{name_prefix}"
  environment = "{env}"
  aws_region  = "{intent.region}"
{_foundation_inputs(stack_name)}}}
'''


def _module_readme(stack_name: str) -> str:
    scope_note = ""
    if stack_name == "foundation":
        scope_note = (
            "\nThis foundation module owns shared AWS primitives that downstream "
            "resources depend on, such as VPCs, subnets, routes, and network "
            "egress. Workload modules should consume its outputs instead of "
            "hiding these resources inside narrowly scoped modules.\n"
        )
    return f"""# {stack_name}

Terraform module generated by IaC Smith.{scope_note}
<!-- BEGIN_TF_DOCS -->
<!-- END_TF_DOCS -->
"""


def _uses_foundation(stack_name: str) -> bool:
    return stack_name in {"ecs-fargate"}


